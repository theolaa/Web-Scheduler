function addMultiDayClass(course) {
  var noOfDays = course.days.length
  var i = 0
  while (i < noOfDays) {
    var temp = {
      crn:course.crn,
      subj:course.subj,
      name:course.name,
      sectionId:course.sectionId,
      teacher:course.teacher,
      location:course.location,
      days:course.days.charAt(i),
      start:course.start,
      finish:course.finish
    }
    sectionsToDisplay.push(temp)
    i++
  }
}

function getDaysOfWeek(course) {
  var finalDays = ""

  if (course.days.includes("0")) finalDays += "Sun. "
  if (course.days.includes("1")) finalDays += "Mon. "
  if (course.days.includes("2")) finalDays += "Tue. "
  if (course.days.includes("3")) finalDays += "Wed. "
  if (course.days.includes("4")) finalDays += "Thu. "
  if (course.days.includes("5")) finalDays += "Fri. "
  if (course.days.includes("6")) finalDays += "Sat. "

  return finalDays
}

function sortSections() {
	var swapped;
	do {
		swapped = false;
		for (var i = 0; i < sections.length - 1; i++) {
			if (sections[i].subj > sections[i + 1].subj) {
				var temp = sections[i];
				sections[i] = sections[i + 1];
				sections[i + 1] = temp;
				swapped = true;
			}
		}
	} while (swapped);
}

function sortSectionsToDisplay() {
  var swapped;
	do {
		swapped = false;
		for (var i = 0; i < sectionsToDisplay.length - 1; i++) {
			if (convertToMinutes(sectionsToDisplay[i]).start > convertToMinutes(sectionsToDisplay[i + 1]).start) {
				var temp = sectionsToDisplay[i];
				sectionsToDisplay[i] = sectionsToDisplay[i + 1];
				sectionsToDisplay[i + 1] = temp;
				swapped = true;
			}
		}
	} while (swapped);
}

function convertToMinutes(course) {

  day = parseInt(course.days.charAt(0))
  startHours = parseInt(course.start.substring(0, 2))
  startMinutes = parseInt(course.start.substring(2))
  endHours = parseInt(course.finish.substring(0, 2))
  endMinutes = parseInt(course.finish.substring(2))

  return {start:(day*1440) + (startHours*60) + startMinutes,end:(day*1440) + (endHours*60) + endMinutes}

}

function getTimeString(totalMinutes) {
  var days, hours, minutes
  if (totalMinutes > 59) {
    days = Math.floor(totalMinutes/1440)
    totalMinutes -= days*1440
    hours = Math.floor(totalMinutes/60)
    totalMinutes -= hours*60
    minutes = totalMinutes
    return (days + " Days, " + hours + " hours, " + minutes + " minutes.")
  } else {
    return (totalMinutes + " minutes.")
  }
}

